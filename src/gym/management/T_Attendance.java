    private void txttimeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txttimeMouseClicked
        String i=txtid.getText();
        if(i.isEmpty()||!i.matches("\\d+")){
            JOptionPane.showMessageDialog(this,"Please Enter Valid Id");
            return;
        }
        int id=Integer.parseInt(i);
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/gym","root","Admin");
            String  q="select time from trainer where id=?";
            PreparedStatement ps=con.prepareStatement(q);
            ps.setInt(1,id);
            ResultSet rs=ps.executeQuery();
            while(rs.next()){
                txttime.setText(rs.getString(1));
            }
        }
        catch(Exception e){
            System.out.println("Error: "+e.getMessage());
        }
    }//GEN-LAST:event_txttimeMouseClicked


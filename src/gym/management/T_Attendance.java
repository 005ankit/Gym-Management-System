    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        String i=txtid.getText();
        if(i.isEmpty()||!i.matches("\\d+")){
            JOptionPane.showMessageDialog(this,"Please Enter Valid Id");
            return;
        }
        int id=Integer.parseInt(i);
        String time=txttime.getText();
        String attendance;
        if(rba.isSelected()){
            attendance="Absent";
        }
        else if(rbp.isSelected()){
            attendance="Present";
        }
        else{
            JOptionPane.showMessageDialog(this,"Please Give Attendance");
            return;
        }
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/gym","root","Admin");
            String q="insert into T_Attendance values(?,?,?,?)";
            PreparedStatement ps=con.prepareStatement(q);
            ps.setInt(1,id);
                    try{
                        java.util.Date utilDate1 = dcdate.getDate();
                        long milliseconds1 = utilDate1.getTime();
                        java.sql.Date sqlDate1 = new java.sql.Date(milliseconds1);
                        ps.setDate(2,sqlDate1);
                    }
                    catch(Exception ex){
                        JOptionPane.showMessageDialog(this,"Please Enter Date");
                        return;
                    }
            ps.setString(3,attendance);
            ps.setString(4,time);
            int a=ps.executeUpdate();
            if(a>0){
                JOptionPane.showMessageDialog(this,"Attendance Done");
                new T_Attendance().setVisible(true);
                dispose();
            }
        }
        catch(Exception e){
            System.out.println("Error: "+e.getMessage());
        }
    }//GEN-LAST:event_btnsaveActionPerformed

